function thickHeightCalcFunc() {
  // total height of thick redwoods
  const treesTotalHeightDecimal = thickTreesTotalHeight;
  const totalTotalHeightFormattedHTML =
    treesTotalHeightDecimal.toLocaleString('en-US');
  document.getElementById(
    'trees-total'
  ).innerHTML = `Total height for all the thickest trees stacked on top of each other: ${totalTotalHeightFormattedHTML} feet!`;

  // average height of tall redwoods
  const treesTotalAverage =
    thickTreesTotalHeightArr.reduce((a, b) => a + b, 0) /
    thickTreesTotalHeightArr.length;
  document.getElementById(
    'trees-avg-height'
  ).innerHTML = `Average height for all redwood trees: ${treesTotalAverage.toFixed(
    2
  )} feet.`;

  // tallest redwood
  const tallestRedwood = Math.max(...thickTreesTotalHeightArr);
  document.getElementById(
    'tallest-tree'
  ).innerHTML = `The tallest redwood is <b>${stratosphereGiant.name}</b> which is ${tallestRedwood} feet tall.`;

  // shortest  thick redwood
  const shortestRedwood = Math.min(...thickTreesTotalHeightArr);
  document.getElementById(
    'shortest-tree'
  ).innerHTML = `The shortest redwood is <b>${fesslerTree.name}</b> which is ${shortestRedwood} feet tall.`;

  // height diff
  const heightDifference = stratosphereGiant.height - fesslerTree.height;
  document.getElementById(
    'height-difference'
  ).innerHTML = `The height difference between <b>${stratosphereGiant.name}</b> and <b>${fesslerTree.name}</b> is ${heightDifference} feet.`;
}

